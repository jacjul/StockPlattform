        max(value) FILTER (WHERE metric_key = 'total_operating_income_as_reported') AS total_operating_income_as_reported,
        max(value) FILTER (WHERE metric_key = 'diluted_average_shares') AS diluted_average_shares,
        max(value) FILTER (WHERE metric_key = 'basic_average_shares') AS basic_average_shares,
        max(value) FILTER (WHERE metric_key = 'diluted_eps') AS diluted_eps,
        max(value) FILTER (WHERE metric_key = 'basic_eps') AS basic_eps,
        max(value) FILTER (WHERE metric_key = 'diluted_ni_availto_com_stockholders') AS diluted_ni_availto_com_stockholders,
