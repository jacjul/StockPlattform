        symbol,
        jsondump,
        created_at
    FROM market_data.financials_staging
    ORDER BY symbol, created_at DESC NULLS LAST
),
financial_rows AS (
    SELECT
        lf.symbol,
        r.year::int AS year,
        lower(regexp_replace(r.metric, '[^a-zA-Z0-9]+', '_', 'g')) AS metric_key,
        r.value::numeric AS value
    FROM latest_financials lf
    CROSS JOIN LATERAL jsonb_to_recordset(lf.jsondump) AS r(metric text, year int, value numeric)
    WHERE r.year IS NOT NULL
),
financials_pivot AS (
    SELECT
        symbol,
        year,
        max(value) FILTER (WHERE metric_key = 'tax_effect_of_unusual_items') AS tax_effect_of_unusual_items,
